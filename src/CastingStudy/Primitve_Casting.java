package CastingStudy;

public class Primitve_Casting {

	public static void main(String[] args) {
		
		int a=5;//lower data type information
		System.out.println(a);
		
		double b=a;//converting to higher data type
		System.out.println(b);
		
		double z=10.5;//higher datatype
		System.out.println(z);
		
		int y=(int)z;//converted to lower type info called explicite casting or narrowing casting
		System.out.println(y);

	}

}
