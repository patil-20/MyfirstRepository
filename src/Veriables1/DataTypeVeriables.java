package Veriables1;

public class DataTypeVeriables {

	public static void main(String[] args) {
		
		//Primitive datatype
		//1.( Numeric + non-decimal)

		byte A;
		A=127;
		System.out.println("Value of variable A "+A);
		
		short B;
		B=-31000;
		System.out.println("Value of variable B "+B);
		
		int C;
		C=2134567891;
		System.out.println("Value of variable C "+C);
		
		long D;
		D=1000000000;
		System.out.println("Value of variable D "+D);
	
        //2.(Numeric+ decimal)
	
	   float E;
	   E=1555.555f;
	   System.out.println("Value of variable E "+E);
	   
	   double F;
	   F=2222222.222222d;
	   System.out.println("Value of variable F "+F);
	   
	   char grade;
	   grade='A';
	   System.out.println("grade type "+grade);
	   
	   boolean result;
	   result=false;
	   System.out.println("result is "+result);
	   
	   //Non primitive datatype
	   
	   String name;
	   name= "Shubham";
	   System.out.println("My Husband name is "+name);
	   
	}  
}
